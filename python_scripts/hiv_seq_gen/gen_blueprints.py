# Option 1 — distribution comparison (real vs generated)
# ---------------------------------------------------------------------------

def compare_distributions_option1(
    params: dict,
    df_seg: pd.DataFrame,
    n_generated: int = 200,
    rng: np.random.Generator = None,
) -> None:
    """
    Generate `n_generated` blueprints from Option 1 and compare ALL inferred
    distributions against the real data side by side:

      - n_breakpoints per CRF    (NegBin; one integer per CRF)
      - n_subtypes per CRF       (empirical PMF; one integer per CRF)
      - pure segment lengths      (log-normal; one value per pure segment)
      - compound zone lengths     (log-normal; one value per compound zone)
    """
    if rng is None:
        rng = np.random.default_rng()

    # Collect generated statistics
    gen_starts      = []
    gen_ends        = []
    n_bp_gen         = []
    n_st_gen         = []
    pure_len_gen     = []
    compound_len_gen = []

    for _ in range(n_generated):
        bp = sample_blueprint_option1(params, rng=rng)

        gen_starts.append(bp[0][0])
        gen_ends.append(bp[-1][1])

        pure_segs     = [(s, e, ids) for s, e, ids in bp if len(ids) == 1]
        compound_segs = [(s, e, ids) for s, e, ids in bp if len(ids) > 1]

        n_bp_gen.append(len(pure_segs) - 1)
        st_ids = {sid for _, _, ids in bp for sid in ids}
        n_st_gen.append(len(st_ids))

        pure_len_gen.extend(e - s for s, e, _ in pure_segs)
        compound_len_gen.extend(e - s for s, e, _ in compound_segs)

    # Real data
    real_starts = df_seg.groupby('crf')['start'].min()
    real_ends   = df_seg.groupby('crf')['end'].max()
    is_compound_mask  = df_seg['subtype'].str.contains('/', regex=False)
    df_pure           = df_seg[~is_compound_mask]
    df_compound       = df_seg[ is_compound_mask]

    n_pure_per_crf    = df_pure.groupby('crf').size()
    n_bp_real         = (n_pure_per_crf - 1).clip(lower=0)
    n_subtypes_real   = df_pure.groupby('crf')['subtype'].nunique()
    pure_len_real     = df_pure['length'].dropna().astype(float)
    pure_len_real     = pure_len_real[pure_len_real > 0]
    compound_len_real = df_compound['length'].dropna().astype(float)
    compound_len_real = compound_len_real[compound_len_real > 0]

    # ------------------------------------------------------------------ #
    # Printing helpers                                                     #
    # ------------------------------------------------------------------ #
    W_LABEL = 30
    W_COL   = 12

    def _header(title: str) -> None:
        print(f"\n  -- {title} --")
        print(f"  {'':>{W_LABEL}}  {'real':>{W_COL}}  {'generated':>{W_COL}}")
        print(f"  {'-' * (W_LABEL + 2 * W_COL + 4)}")

    def _row(label: str, real_val, gen_val, fmt: str = '.1f') -> None:
        def _fmt(v):
            if v is None or (isinstance(v, float) and np.isnan(v)):
                return 'n/a'.rjust(W_COL)
            return format(v, fmt).rjust(W_COL)
        print(f"  {label:>{W_LABEL}}  {_fmt(real_val)}  {_fmt(gen_val)}")

    def _continuous_block(label: str, real_s: pd.Series, gen_list: list) -> None:
        """Descriptive stats + log-normal fit for a continuous length distribution."""
        _header(label)
        gen_s = pd.Series(gen_list, dtype=float)
        gen_s = gen_s[gen_s > 0]
        r, g  = real_s, gen_s

        _row('n',            len(r),          len(g),          fmt='d')
        _row('min (bp)',     r.min()    if len(r) else float('nan'), g.min()    if len(g) else float('nan'))
        _row('median (bp)',  r.median() if len(r) else float('nan'), g.median() if len(g) else float('nan'))
        _row('mean (bp)',    r.mean()   if len(r) else float('nan'), g.mean()   if len(g) else float('nan'))
        _row('std (bp)',     r.std()    if len(r) else float('nan'), g.std()    if len(g) else float('nan'))
        _row('max (bp)',     r.max()    if len(r) else float('nan'), g.max()    if len(g) else float('nan'))
        if len(r) > 1 and len(g) > 1:
            _row('log-normal μ', np.log(r).mean(), np.log(g).mean(), fmt='.3f')
            _row('log-normal σ', np.log(r).std(),  np.log(g).std(),  fmt='.3f')

    def _discrete_block(label: str, real_s: pd.Series, gen_list: list) -> None:
        """Mean/std + full PMF comparison for a discrete per-CRF distribution."""
        _header(label)
        gen_s = pd.Series(gen_list, dtype=int)
        r, g  = real_s, gen_s

        _row('n (CRFs)', len(r),    len(g),    fmt='d')
        _row('mean',     r.mean(),  g.mean())
        _row('std',      r.std(),   g.std())
        _row('min',      r.min(),   g.min(),   fmt='.0f')
        _row('max',      r.max(),   g.max(),   fmt='.0f')

        # Full PMF
        all_vals = sorted(set(r.tolist()) | set(g.tolist()))
        real_pmf = r.value_counts(normalize=True)
        gen_pmf  = g.value_counts(normalize=True)
        print(f"\n  {'value':>{W_LABEL}}  {'real (%)':>{W_COL}}  {'gen (%)':>{W_COL}}")
        for v in all_vals:
            _row(str(v), real_pmf.get(v, 0.0) * 100, gen_pmf.get(v, 0.0) * 100, fmt='.1f')

    # ------------------------------------------------------------------ #
    # Print all four comparisons                                          #
    # ------------------------------------------------------------------ #
    print(f"\n  === Distribution comparison: real vs {n_generated} generated CRFs ===")
    _discrete_block('Genome Start Position', real_starts, gen_starts)
    _discrete_block('Genome End Position',   real_ends,   gen_ends)
    _discrete_block('n_breakpoints per CRF',   n_bp_real,        n_bp_gen)
    _discrete_block('n_subtypes per CRF',       n_subtypes_real,  n_st_gen)
    _continuous_block('pure segment lengths',   pure_len_real,    pure_len_gen)
    print("\n  Note: compound zones are optional and may be disabled to simplify the model, they are currently " + ("enabled." if add_compound_zones else "disabled."))
    _continuous_block('compound zone lengths',  compound_len_real, compound_len_gen)


# ---------------------------------------------------------------------------
# Option 2 — blueprint replay
# ---------------------------------------------------------------------------

def build_blueprints_option2(df_seg: pd.DataFrame) -> list:
    """
    Option 2: encode each real CRF structure as a blueprint.
    Blueprints are lists of (start, end, subtype_id_list) where
    subtype IDs are local per-blueprint integers (so the generator
    can remap them to any real subtype at generation time).
    """
    all_blueprints = []
    for crf, group in df_seg.groupby('crf', sort=False):
        subtype_id_dict = {}
        blueprint = []
        for _, row in group.iterrows():
            subtypes = row['subtype'].split('/') if '/' in row['subtype'] else [row['subtype']]
            for st in subtypes:
                if st not in subtype_id_dict:
                    subtype_id_dict[st] = len(subtype_id_dict)
            blueprint.append((row['start'], row['end'], [subtype_id_dict[st] for st in subtypes]))
        all_blueprints.append(blueprint)
    return all_blueprints


