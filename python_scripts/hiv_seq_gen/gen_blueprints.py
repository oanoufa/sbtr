# Option 1 — blueprint sampling
# ---------------------------------------------------------------------------

def sample_blueprint_option1(
    params: dict,
    rng: np.random.Generator = None,
) -> list:
    """
    Sample a novel blueprint from the inferred generative parameters.

    The procedure is:
      1. Sample number of pure breakpoints from the negative binomial.
      2. Sample breakpoint positions subject to a minimum inter-breakpoint gap
         of `min_seg_len` nt (derived from the observed minimum pure segment
         length), so that no pure segment shorter than that can be generated.
      3. Assign subtypes to pure segments.
      4. For each internal breakpoint, with probability compound_prob, insert
         a compound zone of sampled length carved from the right edge of the
         left pure segment.  The compound zone borrows from the pure segment
         so the total genome span is preserved.

    Returns a list of (start, end, subtype_id_list) with local integer
    subtype IDs. The generator remaps these to real subtypes at call time.
    """
    if rng is None:
        rng = np.random.default_rng()

    min_seg_len = params.get('min_seg_len', 1)

    # 1. Sample genome boundaries
    # start_vals  = params['genome_start']['values']
    # start_probs = params['genome_start']['probs']
    # genome_start = int(rng.choice(start_vals, p=start_probs))

    # end_vals    = params['genome_end']['values']
    # end_probs   = params['genome_end']['probs']
    # genome_end   = int(rng.choice(end_vals, p=end_probs))

    # In the end we want to generate full-length CRFs that span the entire genome
    genome_start = 1
    genome_end = 9719

    # Sanity check: Ensure valid boundaries (fallback to HXB2 bounds just in case)
    if genome_start >= genome_end:
        genome_start, genome_end = 1, 9719
    # 1. Sample number of pure breakpoints
    nb = params['n_breakpoints']
    n_bp = int(rng.negative_binomial(nb['r'], nb['p']))
    n_bp = max(1, n_bp)

    # 2. Place breakpoints with minimum-gap constraint
    bp_positions = _place_breakpoints(n_bp, genome_start, genome_end, min_seg_len, rng)

    # 3. Build pure segments
    boundaries = [genome_start] + bp_positions + [genome_end]
    n_segments  = len(boundaries) - 1

    # 4. Assign subtypes
    n_st_vals  = params['n_subtypes']['values']
    n_st_probs = params['n_subtypes']['probs']
    n_subtypes = int(rng.choice(n_st_vals, p=n_st_probs))
    n_subtypes = max(1, min(n_subtypes, n_segments))

    base_ids = list(range(n_subtypes))
    if n_segments > n_subtypes:
        base_ids += rng.integers(0, n_subtypes, size=n_segments - n_subtypes).tolist()
    rng.shuffle(base_ids)

    # 5. Build the blueprint with optional compound zones at breakpoints
    cl    = params['compound_lengths']
    cprob = params['compound_prob']

    blueprint = []
    for i in range(n_segments):
        seg_start = boundaries[i]
        seg_end   = boundaries[i + 1]
        left_id   = base_ids[i]
        right_id  = base_ids[i + 1] if i + 1 < n_segments else None

        # Insert compound zone with probability cprob if this is an internal breakpoint long enough
        insert_compound = (
            right_id is not None
            and right_id != left_id       # real subtype boundary
            and (seg_end - seg_start) >= min_seg_len*2  # enough room to carve out a compound zone
            and rng.random() < cprob
        )

        if insert_compound and add_compound_zones:
            # Sample compound-zone length from the log-normal
            raw_len = int(np.round(rng.lognormal(cl['mu_log'], cl['std_log'])))
            # Clamp: at most half the current pure segment, leaving at least 1 nt pure
            max_cz  = max(1, (seg_end - seg_start) // 2)
            cz_len  = max(1, min(raw_len, max_cz))

            pure_end = seg_end - cz_len
            if pure_end > seg_start:
                blueprint.append((seg_start, pure_end, [left_id]))
                blueprint.append((pure_end,  seg_end,  [left_id, right_id]))
            else:
                # Segment too short to split — label whole thing compound
                blueprint.append((seg_start, seg_end, [left_id, right_id]))
        else:
            blueprint.append((seg_start, seg_end, [left_id]))

    return blueprint


