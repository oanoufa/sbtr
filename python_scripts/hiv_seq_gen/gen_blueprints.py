def _fit_lognormal(lengths: pd.Series) -> dict:
    """Fit a log-normal to a Series of positive lengths and return parameters."""
    lengths = lengths.dropna().astype(float)
    lengths = lengths[lengths > 0]
    mu_log  = float(np.log(lengths).mean())
    std_log = float(np.log(lengths).std())
    return {
        'distribution': 'lognormal',
        'mu_log':  mu_log,
        'std_log': std_log,
        'min':     int(lengths.min()),
        'max':     int(lengths.max()),
        'median':  float(lengths.median()),
        'n':       int(len(lengths)),
    }


def _print_lognormal(label: str, d: dict) -> None:
    print(f"  {label:35s}  n={d['n']}  median={d['median']:.0f} bp  "
          f"log-normal(μ={d['mu_log']:.2f}, σ={d['std_log']:.2f})  "
          f"[{d['min']}, {d['max']}]")


def _place_breakpoints(
    n_bp: int,
    genome_start: int,
    genome_end: int,
    min_seg_len: int,
    rng: np.random.Generator,
) -> list:
    """
    Sample `n_bp` breakpoint positions on [genome_start, genome_end] such that
    every resulting segment (including the two flanking ones) is at least
    `min_seg_len` nt long.

    Uses the stars-and-bars trick: reduce the usable span by the minimum
    padding required for n_bp+1 segments, sample uniformly in the reduced
    space, then map back.

    If the genome is too short to fit `n_bp` breakpoints with the minimum
    gap, the number of breakpoints is silently reduced to the maximum
    feasible value.
    """
    span = genome_end - genome_start
    # n_bp breakpoints create n_bp+1 segments, each needing min_seg_len nt.
    # After reserving that minimum, the remaining slack is distributed freely.
    max_bp = max(0, (span // min_seg_len) - 1)
    n_bp   = min(n_bp, max_bp)

    if n_bp == 0:
        return []

    reduced_span = span - (n_bp + 1) * min_seg_len
    # Draw n_bp offsets from [0, reduced_span] without replacement, sort them
    raw = sorted(rng.choice(np.arange(0, reduced_span + 1), size=n_bp, replace=False).tolist())
    # Map back: i-th breakpoint = genome_start + raw[i] + (i+1)*min_seg_len
    breakpoints = [genome_start + raw[i] + (i + 1) * min_seg_len for i in range(n_bp)]
    return breakpoints


