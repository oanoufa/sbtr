"""
Parameter inference for the HIV sequence generator.

Option 1: Generative model — infer statistical parameters from real CRFs,
          then sample new blueprint structures at generation time.
          Allows the model to encounter novel breakpoint configurations.

    Chosen distributions:
    Negative binomial for number of breakpoints
    The number of breakpoints per CRF is a count variable (non-negative integer), so we need a discrete distribution.
    The simplest choice would be Poisson, but Poisson has a hard constraint that mean = variance.
    The negative binomial adds a dispersion parameter that decouples mean and variance.
    If we fit it and find variance ≈ mean, it degrades toward Poisson.

    Log-normal for regular segment lengths (diagnostic only — not used in generation)
    Segment lengths are positive continuous values with a right-skewed distribution. Most segments are short to moderate,
    but some are very long (a single subtype dominating most of the genome).
    NOTE: segment lengths are fully determined by the number of breakpoints and their sampled positions, so this
    distribution is not used in generation — it is inferred and printed only for comparison against generated data.

    Empirical PMF for number of distinct subtypes
    The number of distinct subtypes per CRF is a small discrete variable with a very constrained range (2 to 6).
    Hence we are just using the observed frequencies directly.

    Compound segments as breakpoint-uncertainty zones
    A compound segment (e.g. A/B) models uncertainty at a subtype boundary: a small stretch of the genome where
    the transition between two flanking subtypes is ambiguous. Accordingly, compound segments are treated as a
    distinct layer on top of the regular (pure) segments:
      - compound_prob   : Bernoulli probability that a given internal breakpoint gets a compound zone
      - compound_lengths: lengths of these zones, fitted as a log-normal (expected to be short)
    Compound segments are excluded from the regular segment-length distribution so the two are not confounded.

    Minimum segment length
    Breakpoint positions are drawn subject to a minimum inter-breakpoint gap derived from the observed minimum
    pure segment length in the real data.  This prevents biologically implausible 1 nt segments from being
    generated by the uniform breakpoint placement step.

Option 2: Blueprint replay — use real CRF architectures as-is.
          Preserves biological realism but limits to known recombination patterns.
"""

