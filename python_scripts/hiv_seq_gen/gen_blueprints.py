# Option 1 — parameter inference
# ---------------------------------------------------------------------------

def infer_params_option1(df_seg: pd.DataFrame, df_bp: pd.DataFrame) -> dict:
    """
    Option 1: infer generative parameters from real CRF structures.

    Parameters inferred
    -------------------
    n_breakpoints       : distribution of number of *pure* breakpoints per CRF
                          (fitted as a negative binomial)
    segment_lengths     : distribution of *pure* (non-compound) segment lengths
                          (fitted as a log-normal — diagnostic only, not used in generation)
    min_seg_len         : minimum pure segment length observed in the real data;
                          used as the hard lower bound when placing breakpoints
    n_subtypes          : distribution of distinct subtypes per CRF
                          (empirical PMF — small discrete range)
    compound_prob       : probability that a given internal breakpoint gets a
                          flanking compound zone (Bernoulli)
    compound_lengths    : distribution of compound-zone lengths
                          (fitted as a log-normal)

    Compound segments are treated as breakpoint-uncertainty zones inserted
    between two pure segments, so they are excluded from the regular segment
    length distribution.
    """
    params = {}

    # ------------------------------------------------------------------ #
    # Split df_seg into pure and compound rows                            #
    # ------------------------------------------------------------------ #
    is_compound_mask = df_seg['subtype'].str.contains('/', regex=False)
    df_pure     = df_seg[~is_compound_mask].copy()
    df_compound = df_seg[ is_compound_mask].copy()

    # ------------------------------------------------------------------ #
    # Number of breakpoints per CRF                                       #
    # Count only pure segments; compound zones sit *between* pure ones    #
    # and are not counted as independent segments for this distribution.  #
    # n_breakpoints = n_pure_segments - 1                                 #
    # ------------------------------------------------------------------ #
    n_pure_per_crf = df_pure.groupby('crf').size()
    n_bp_per_crf   = (n_pure_per_crf - 1).clip(lower=0)

    mu  = n_bp_per_crf.mean()
    var = n_bp_per_crf.var()

    if var > mu:
        r_nb = mu**2 / (var - mu)
        p_nb = mu / var
    else:
        # Variance ≤ mean → degenerate toward Poisson
        r_nb = 1e6
        p_nb = mu / (mu + 1)

    params['n_breakpoints'] = {
        'distribution': 'negative_binomial',
        'r': r_nb,
        'p': p_nb,
        'empirical_mean': mu,
        'empirical_var':  var,
        'empirical_counts': n_bp_per_crf.value_counts().sort_index().to_dict(),
    }

    # ------------------------------------------------------------------ #
    # Segment lengths — pure segments only (diagnostic)                   #
    # Also derive min_seg_len for constrained breakpoint placement.       #
    # ------------------------------------------------------------------ #
    pure_lengths = df_pure['length'].dropna().astype(float)
    pure_lengths = pure_lengths[pure_lengths > 0]
    params['segment_lengths'] = _fit_lognormal(pure_lengths)
    params['min_seg_len'] = int(pure_lengths.min())

    # ------------------------------------------------------------------ #
    # Number of distinct subtypes per CRF (from pure segments)            #
    # ------------------------------------------------------------------ #
    n_subtypes_per_crf = df_pure.groupby('crf')['subtype'].nunique()
    pmf = n_subtypes_per_crf.value_counts(normalize=True).sort_index()
    params['n_subtypes'] = {
        'distribution': 'empirical',
        'values':       pmf.index.tolist(),
        'probs':        pmf.values.tolist(),
    }

    # ------------------------------------------------------------------ #
    # Compound zones                                                       #
    #                                                                     #
    # compound_prob : fraction of internal breakpoints that have a        #
    #                 compound zone.  Denominator = total internal         #
    #                 breakpoints across all CRFs = sum(n_pure - 1).      #
    # compound_lengths : log-normal fit on compound segment lengths.      #
    # ------------------------------------------------------------------ #
    n_internal_bp_total = int(n_bp_per_crf.sum())
    n_compound_total    = len(df_compound)

    compound_prob = (n_compound_total / n_internal_bp_total
                     if n_internal_bp_total > 0 else 0.0)
    params['compound_prob'] = float(compound_prob)
    params['compound_lengths'] = _fit_lognormal(df_compound['length'])

    # ------------------------------------------------------------------ #
    # Genome start and end boundaries                                    #
    # ------------------------------------------------------------------ #
    # The true start of a CRF is the minimum start position of its segments
    # The true end is the maximum end position of its segments
    crf_starts = df_seg.groupby('crf')['start'].min()
    crf_ends   = df_seg.groupby('crf')['end'].max()

    start_pmf = crf_starts.value_counts(normalize=True).sort_index()
    end_pmf   = crf_ends.value_counts(normalize=True).sort_index()

    params['genome_start'] = {
        'distribution': 'empirical',
        'values': start_pmf.index.astype(int).tolist(),
        'probs':  start_pmf.values.tolist(),
    }
    
    params['genome_end'] = {
        'distribution': 'empirical',
        'values': end_pmf.index.astype(int).tolist(),
        'probs':  end_pmf.values.tolist(),
    }

    # ------------------------------------------------------------------ #
    # Print summary                                                        #
    # ------------------------------------------------------------------ #
    print("\n  === Option 1 parameters ===")
    print(f"  Pure breakpoints/CRF  mean={mu:.1f}  var={var:.1f}  "
          f"(NegBin r={r_nb:.2f} p={p_nb:.3f})")
    print(f"  Min pure segment length (min_seg_len): {params['min_seg_len']} bp")
    _print_lognormal("Pure segment lengths (diag.)", params['segment_lengths'])
    _print_lognormal("Compound zone lengths",         params['compound_lengths'])
    print(f"  Compound prob per internal breakpoint: {compound_prob:.3f}  "
          f"({n_compound_total}/{n_internal_bp_total})")
    print(f"  Distinct subtypes per CRF:\n{pmf.to_string()}")

    return params


